package it.univaq.disim.swa.visitaq.business;

public class EventResourceBusinessException extends Exception {

	public EventResourceBusinessException() {
		super();
	}
	public EventResourceBusinessException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}
	public EventResourceBusinessException(String message, Throwable cause) {
		super(message, cause);
	}
	public EventResourceBusinessException(String message) {
		super(message);
	}
	public EventResourceBusinessException(Throwable cause) {
		super(cause);
	}
}
