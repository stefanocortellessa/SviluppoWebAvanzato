package it.univaq.disim.swa.visitaq.business;

public class AttractionResourceBusinessException extends Exception {

	public AttractionResourceBusinessException() {
		super();
	}
	public AttractionResourceBusinessException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}
	public AttractionResourceBusinessException(String message, Throwable cause) {
		super(message, cause);
	}
	public AttractionResourceBusinessException(String message) {
		super(message);
	}
	public AttractionResourceBusinessException(Throwable cause) {
		super(cause);
	}
}
