package it.univaq.disim.swa.visitaq.business;

public class AttractionManagerBusinessException extends Exception {

	public AttractionManagerBusinessException() {
		super();
	}
	public AttractionManagerBusinessException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}
	public AttractionManagerBusinessException(String message, Throwable cause) {
		super(message, cause);
	}
	public AttractionManagerBusinessException(String message) {
		super(message);
	}
	public AttractionManagerBusinessException(Throwable cause) {
		super(cause);
	}
}
